"""
hook.py
오디오 드라마 훅(오프닝) 생성 모듈
"""

HOOK_PROMPT = """
【제목】
{title}

【전체 개요】
{outline_full}

【과제】
스토리의 가장 극적인 순간을 500자로 작성하세요.

━━━━━━━━━━━━━━━━
⭐ 훅 구조 ⭐
━━━━━━━━━━━━━━━━

1단계: 강렬한 대사 한 줄 (5-15자)

좋은 예:
"어머니, 이제 그만 죽어주세요."
"이 시계 우리 아빠 거예요."
"저 사람이 제 아들을 죽였어요."

나쁜 예:
"안녕하세요."
"어떻게 지내셨어요?"

2단계: 상황 설명 나레이션 (300자)
- 누가, 어디서, 무슨 일이 벌어졌는지
- 충격적인 사실 제시 (닮은 얼굴, 숨겨진 비밀, 예상치 못한 만남 등)
- 갈등의 핵심 암시

3단계: 의문 제기로 마무리 (30-50자)
"그렇다면 눈앞의 이 소년은 도대체 누구인 걸까요?"
"18년 전 그날 밤, 무슨 일이 있었던 걸까요?"

【필수 규칙】

1. 화자: 전지적 3인칭

2. 대사는 맨 처음 한 줄만
나머지는 전부 나레이션

3. 감정은 신체 반응
"놀랐다" (X) → "숨이 멎는 듯했습니다" (O)
"슬펐다" (X) → "눈물이 흘렀어요" (O)

4. 오디오 중심
소리, 목소리 톤, 의성어 활용

5. 마지막은 반드시 의문문

【출력】
순수 텍스트 500자

지금 바로 작성하세요.
"""


def generate_hook_prompt(title: str, outline_full: str) -> str:
    """
    훅 생성 프롬프트 생성

    Args:
        title: 오디오 드라마 제목
        outline_full: 전체 개요

    Returns:
        완성된 프롬프트
    """
    return HOOK_PROMPT.format(
        title=title,
        outline_full=outline_full
    )
